
import java.util.Scanner;

public class P369 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n;
         
        do {
            n = sc.nextInt();

            for (int i = 0; i < n; i++) {
                System.out.print(1);
            }
            if (n != 0){
                System.out.println();
            }

        }while (n != 0);
       
        
    }
    
}
